package com.diviso.model;
public class Car
{
	private String name;
	private String colour;
	

		public void setName(String name)
		{
			this.name=name;
		}
		public String getName()
		{ 
			return name;
		}
				
		public void setColour(String colour)
		{
			this.colour=colour;
		}
		public String getColour()
		{
			return colour;		
		}
}