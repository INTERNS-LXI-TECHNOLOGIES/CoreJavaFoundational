public class Tyre {

	String tyrePosition, tyreType, brand, radius;

	public void printDetails() {
		System.out.println("Position of Tyre is : " + this.tyrePosition);
		System.out.println("Type of Tyre is : " + this.tyreType);
		System.out.println("Brand of Tyre is : " + this.brand);
		System.out.println("Radius of Tyre is : " + this.radius);
	}
}