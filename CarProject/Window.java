public class Window{
String window;
int size ;

public String getWindow()
{
	return window;
}

public void setWindow(String window)
{
	this.window = window;
}

public int getSize()
{
	return size;
}

public void setSize(int size)
{
	this.size = size;
}



public String toString()
{
	
   return window+""+"\n"+size+"";	
	
}
}