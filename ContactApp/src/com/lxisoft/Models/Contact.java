package com.lxisoft.Models;

public class Contact
{
	private String name;
	private String phno;
	public void setName(String name)
	{
		this.name=name;
	}
	public void setNo(String phno)
	{
		this.phno=phno;
	}
	public String getName()
	{
		return name;
	}
	public String getNo()
	{
		return phno;
	}
}