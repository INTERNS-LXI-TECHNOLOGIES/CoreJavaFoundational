package com.lxisoft.hotel;
public class FoodProduct
{
	private String name;
	private int amount;

	public void setName(String newName)
	{
		this.name = newName;
	}
	public String getName()
	{
		return name;
	}
	public void setAmount(int newAmount)
	{
		this.amount = newAmount;
	}
	public int getAmount()
	{
		return amount;
	}
}