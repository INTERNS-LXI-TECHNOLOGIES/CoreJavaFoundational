package com.lxisoft.Hotel;

public class Menu
{
	private String name;
	private int rate;

	

	public void setName(String n)
	{
		name = n;
	}
    
    public String getName()
    {
    	return name;
    }

    public void setRate(int r)
    {
    	rate = r;
    } 

    public int getRate()
    {
    	return rate;
    }
}