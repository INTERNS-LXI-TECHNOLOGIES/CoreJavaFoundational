package com.lxisoft.movie;

public class Dialogue{
  String dialogue;
  Actor deliveredBy;
  public void setActor(){
    this.deliveredBy=deliveredBy;
  }
  public Actor getActor(){
    return deliveredBy;
  }
  public void setDialogue(){
    this.dialogue=dialogue;
  }
  public String getDialogue(){
    return dialogue;
  }
}
