package com.lxisoft.storyline;

import java.io.*;
import java.util.*;
public class Script
{
	String dialogues;

	String scene;

	

	public void setDialogue(String dialogues)
	{
		this.dialogues=dialogues;
	}

	
	public String getDialogue()
	{
		return dialogues;
	}


public void setScene(String scene)
	{
		this.scene=scene;
	}

	
	public String getScene()
	{
		return scene;
	}


}