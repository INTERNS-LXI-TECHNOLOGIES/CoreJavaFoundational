public class Student{
String name;
String rollno;
String regno;
String mark;
public Student(String name, String rollno, String regno, String mark)
{
this.name = name;
this.rollno = rollno;
this.regno = regno;
this.mark = mark;
}
public String toString() {

return "name: " +name+ "\n" +"rollno: "+rollno+ "\n" + "regno: " +regno+ "\n" +"mark: "+mark;
}
}

