package com.lxisoft.Game;

public class Player
{
	private String name;
	private int position;

	public void setName(String n)
	{
       this.name = n;
	}

	public String getName()
	{
		return name;
	}

	public void setPosition(int p)
	{
		this.position = p;
	}

	public int getPosition()
	{
		return position;
	}
}